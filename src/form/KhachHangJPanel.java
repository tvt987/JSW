    boolean isValidation() {

        KhachHang kh = getFormKH();
        if (khdao.selectByID(kh.getMakh()) != null) {
            MsgBox.alert(this, "Mã khách hàng đã tồn tại\n");
            return false;
        }
        if (KhachHang_txtmakh.getText().isEmpty()) {
            MsgBox.alert(this, "Không được để trống mã khách hàng\n");
            return false;
        }
        if (buttonGroup1.getSelection() == null) {
            MsgBox.alert(this, "Chưa chọn giới tính\n");
            return false;
        }
        if (KhachHang_txttenkh.getText().isEmpty()) {
            MsgBox.alert(this, "Không được để trống tên khách hàng\n");
            return false;
        }
        if (KhachHang_txtdienthoai.getText().isEmpty()) {
            MsgBox.alert(this, "Không được để trống số điện thoại\n");
            return false;
        }
        if (!KhachHang_txtdienthoai.getText().matches("^\\d{10}$")) {
            MsgBox.alert(this, "Số điện thoại không hợp lệ\n");
            return false;
        }
        try {
            int diem = Integer.parseInt(KhachHang_txtDiem.getText());
        } catch (Exception e) {
            MsgBox.alert(this, "Điểm không đúng định dạng\n");
            return false;
        }
        if (KhachHang_txtdiachi.getText().isEmpty()) {
            MsgBox.alert(this, "Không được để trống địa chỉ\n");
            return false;
        }
        return true;
    }

